namespace Herramientas
{
    public class Operaciones
    {
        // Atributos

        public int num1;
        public int num2;
        public int resultado;

        // Métodos

        public void Sumar() { resultado = num1 + num2; }
        public void Restar() { resultado = num1 - num2; }
        public void Multiplicar () { resultado = num1 * num2; }
        public void Dividir () { resultado = num1 / num2; }
    }
}